int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	if (nb <= 0)
		return (0);
	else
	{
		while (i <= nb)
		{
			if (i == nb)
				return (j);
			else
			{
				i = i + (2 * j + 1);
				j++;
			}
		}
		return (0);
	}
}
